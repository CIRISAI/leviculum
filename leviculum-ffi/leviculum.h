/*
 * Thread safety and blocking model
 * ================================
 *
 * Handle sharing across threads:
 *   - A `leviculum_t` node handle is internally synchronised: the `lev_*` calls
 *     that take a `leviculum_t*` (announce, send, connect, register, stats,
 *     etc.) may be made concurrently from multiple threads. The hidden runtime
 *     and engine state are mutex-guarded.
 *   - Event draining is single-consumer: do NOT call `lev_next_event` or
 *     `lev_wait_event` for the same node from more than one thread at a time,
 *     and do not mix the two concurrently on the same node.
 *   - `lev_link_t`, `lev_identity_t`, `lev_destination_t`, and `lev_event_t`
 *     handles are not internally locked for concurrent mutation. A handle may
 *     be moved between threads, but do not call into the same handle from two
 *     threads at once. A `lev_*_free` must not race any other call on that
 *     handle.
 *   - `lev_log_set_level` / `lev_log_set_callback` are process-global and
 *     thread-safe.
 *
 * Blocking and the event fd (deadlock warning):
 *   - Calls that take a `timeout_ms` parameter (lev_announce, lev_connect,
 *     lev_link_send, lev_send_request, lev_send_response, lev_send_datagram,
 *     lev_close_link, lev_start, lev_stop, ...) block the calling thread until
 *     they complete or the timeout elapses. A negative timeout blocks forever.
 *   - These blocking calls are driven on the calling thread but depend on the
 *     node's event loop making progress. Do NOT issue a blocking `lev_*` call
 *     from the SAME thread that drains the event fd while it is mid-drain; run
 *     the poll/drain loop and the blocking calls on separate threads, or never
 *     block on the drain thread. Blocking on the drain thread can wedge both.
 *   - `lev_event_fd` returns a fd you add to your own poll/epoll/select loop;
 *     the library owns it and closes it in `lev_free` (never close it yourself).
 */


